public class HelloWorld {
	
	public static void main(String[] args) {
		System.out.println("Hello World");
		System.out.println();
		System.out.print("This is on line three. ");
		System.out.print("This is also on line three");
		System.out.println();
		//println inserts a new line after printing whatever is in the brackets
		// whereas just print doesn't. You can also move to the next line by using "\n"
		System.out.print("This will be on line 5\nThis will be on line 6");
	}

}
