import java.util.*;

public class addOrSubtract {
	public static void main(String[] args) {
		int x = 9;
		System.out.println(x++);
		System.out.println(x);
		
		int y = 0;
		System.out.println(y--);
		System.out.println(y);
		
		int z = 8;
		System.out.println(++z);
		System.out.println(--y);
		
	}
}
